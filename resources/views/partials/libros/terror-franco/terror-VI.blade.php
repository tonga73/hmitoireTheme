								      <div class="modal-body container belle h4">
										<p>
											¿Alguien vio alguna vez un nido de ñacaniná…? ¿Con ñacaniná y todo? 
										</p>
										<p>
											¿No? No saben lo que se pierden.
										</p>
										<p>
											Ver un nido en vivo y en directo es uno de los instantes más estremecedores que puede tener una persona, un instante de miedo al cien por ciento. Pánico en su máximo nivel. ¡Qué momento tan maravilloso!
										</p>
										<p>
											No hay película, televisión, computadora ni jueguito electrónico que pueda llegarle a los talones o brindarnos un espectáculo igual.
										</p>
										<p>
											Obvio, no es lo mismo ver a una víbora por televisión que verla a cinco metros de distancia ¡y sabiendo que te correrá! ¡Pura adrenalina!
										</p>
										<p>
											Les cuento cómo es la cosa. La ñacaniná es una víbora no venenosa (a pesar de que por ahí leerán que es venenosa, el tío Aldo me aseguró que no es venenosa y Sanseacabó). Suele medir hasta dos metros, y su hábitat natural son los esteros y cañadas. Come ranitas, ratitas de pajonales, bichitos en general, gallinas, pollitos y huevos. Es básicamente una víbora constrictora, o sea, se te enrosca y te ahoga como si fueras un cachilito. Pero además, tiene otra característica terrible: ¡corre como un cohete! ¡Qué lo tiró! 
										</p>
										<p>
											Es una de las víboras más veloces, tanto por tierra ¡como en el agua! ¡Hay que ver cómo corre la desgraciada! Y tiene una particularidad que la hace más aterradora aún, y es que cuando navega, saca su cabeza y parte de su cuerpo fuera del agua ¡como el periscopio de un submarino! ¡Imagínense entonces corriendo en la cañada con algo así detrás de sus talones!
										</p>
										<p>
											Yo sabía esto desde chiquito, porque me lo contaban mi tío y mi primo Sergio. En el campo era raro encontrar un chico al que no lo hubiera corrido alguna vez una ñacaniná. ¡A Sergio lo habían corrido más de diez veces! Al final ese asunto terminó transformándose en una especie de deporte: “Huyendo de la ñacaniná” podríamos ponerle de título.
										</p>
										<p>
											Mi primo tenía muchos amigos ahí en Cancha Larga, todos por supuesto, chicos del campo como él. Había dos que eran sus íntimos: los hermanos Eduardo y Raúl Acuña. Los tres ya habían sido alcanzados y mordidos por esta aterradora viborita, y Eduardo era el que tenía el récord: ya lo habían mordido tres veces. La verdad es que me hacían sentir inferior cuando contaban esas cosas, sobre todo porque yo veía una lagartija y salía corriendo.
										</p>
										<p>
											Cuando tenía siete u ocho años, me daba miedo y ni por las tapas quería ir a ver un nido de ñacaniná o cosas parecidas. Pero después de que cumplí los diez, ya era grandecito como para andar achicándome ante cualquier peligro y a pesar de que me moría de miedo, ¡minga que me iba a achicar! Aparte, no quería hacerle pasar vergüenza a Sergio y que le dijeran que tenía un primo pueblerino y miedoso. Por supuesto, era pueblerino y miedoso ¡pero cómo lo disimulaba!
										</p>
										<p>
											Recuerdo la primera vez que fuimos a ver un nido. No sólo era verlo de cerca, sino que había que cascotearlo y embravecer a la víbora ¡y a correr se ha dicho! Ésa era la diversión. Ya me habían explicado para que no me hiciera problema si por ahí me alcanzaba y me daba un mordiscón, que eso no tenía importancia, y había que hacer de cuenta como si nos hubiese arañado un gato o picoteado un loro. Con eso me quedé algo más tranquilo ¡pero igual estaba dispuesto a correr con todas mis energías hasta echar los bofes!
										</p>
										<p>
											Hacia el fondo de la chacra del tío Luis, había una cañada no muy grande, justo al costado de la chacra de los Almanza. Tendría unos doscientos metros de largo por cien de ancho. Ahí había de todo. 
										</p>
										<p>
											La ñacaniná suele hacer su nido en algún montículo de tierra que sobresale del agua, pero siempre dentro de la cañada. Allí deposita sus huevos, y los cuida hasta que salen sus hijitos. Si hay una madre que cuida a su cría ¡sin dudas ésa es la ñacaniná! ¡Qué lo tiró que es brava la señora!
										</p>
										<p>
											La diversión consistía en acercarse de la manera más sigilosa posible desde distintos ángulos, cosa de rodearla por todas partes. Debíamos ir en alerta máxima, mirando fijamente el nido, donde se hallaba enroscada la víbora, y apenas la veíamos que se desenrollaba y comenzaba a sumergirse ¡patitas para qué las quiero y a correr se ha dicho! Porque eso significaba que ya nos había oído y se largaba al ataque. Por supuesto, era una lotería saber a quién correría de todos los que nos acercábamos.
										</p>
										<p>
											En esa oportunidad, tuvimos que caminar bastante —cañada adentro—, hasta divisar el nido. A unos cincuenta metros nos distribuimos los cuatro, cosa de rodearla desde todas partes. El agua nos llegaba casi hasta las rodillas. Yo pedí que me dejaran correr hacia la parte más playa, porque era el más nuevo e inexperto. Me autorizaron por esa vez.
										</p>
										<p>
											Todos llevábamos cascotes, para el caso de que la víbora estuviera muy perezosa. Y efectivamente estaba perezosa. Llegamos hasta unos ocho o diez metros, y la vimos ¡era inmensa! Estaba toda enroscada sobre el montículo de tierra. Nosotros, en silencio absoluto. Nos hicimos señas y preparamos una andanada de cascotazos. “¡Ya!”, gritó Sergio, y todos lanzamos nuestros cascotes.
										</p>
										<p>
											En un santiamén la víbora se desenrolló al tiempo que velozmente se sumergía, y vimos los movimientos de los pastitos y camalotes, por donde iba nadando subacuáticamente ¿y a qué no saben hacia quién se dirigía? ¡¡Sí!! ¡Acertaron! ¡La desgraciada se venía derechito hacia mí! ¡¡Cómo nadaba la loca!! Por supuesto que yo salí como alma que se la lleva el Diablo, y mientras corría, los otros se mataban de risa y me gritaban guarangadas y otras cosas, pero yo sólo miraba el camino y los obstáculos que tenía por adelante ¡miren si me caía! Ahí sí que me agarraba la ñacaniná, me enroscaba y me mordisqueaba todo. Como no miraba para atrás, sólo me guiaba por los gritos de los chicos, y según ellos, parecía que ya estaba a punto de  alcanzarme.
										</p>
										<p>
											Habré corrido unos doscientos metros, chapoteando o atropellando cardos y pajonales, y tal vez la víbora sólo me habrá perseguido unos veinte o treinta metros nomás, o sea que corrí más de ciento cincuenta metros al santo cohete ¡pero qué me importaba! Yo sólo quería asegurarme.
										</p>
										<p>
											Creo que luego de esa experiencia, ya estaba en condiciones de representar a mi país en los juegos olímpicos mundiales. Me sentía el niño más veloz del planeta.
										</p>
										<p>
											Pero como todo juego peligroso —y éste de verdad que era muy peligroso— a veces, en vez de gracioso y divertido suele resultar trágico y aterrador. 
										</p>
										<p>
											Todos en Cancha Larga recuerdan el caso de los hermanitos Ávalos, una familia requetepobre y humilde. Eran tan pobres que tres de sus hijos habían muerto de desnutrición. Ninguno fue a la escuela, porque les quedaba lejísimo. Y con el hambre y la debilidad que tenían sumados a la pobreza, eso era una imposibilidad absoluta. En total eran nueve, pero si les restamos los tres muertos, sólo nos quedan seis. Bueno, de estos seis es que quiero contarles la historia.
										</p>
										<p>
											Cuando ocurrió el hecho, el más grandecito tendría once años y el más chiquito era un bebé de dos meses. Vivían en un lugar muy inhóspito y casi inaccesible, donde sólo se podía llegar a pie o a caballo, atravesando montes, picadas y esteros. Yo recuerdo que desde la panadería del tío Aldo, uno podía llegar hasta ese ranchito si se internaba por una picada muy angosta entre pajonales y montes. La distancia sería de media legua más o menos.
										</p>
										<p>
											El ranchito estaba ubicado a orillas del monte y a unos veinte metros comenzaba un gran estero. Se imaginarán que estos chicos no tenían otro lugar para estar, para jugar o para recorrer que los alrededores de su casa. El humilde padre, todos los días salía a recorrer las chacras de la zona en busca de trabajo o para pedir algo de comida a alguien piadoso. La mayoría de las veces no conseguía ninguna changa, ni tampoco comida. No había ningún lugar cercano para pescar; y para cazar, no tenía siquiera una escopeta, apenas una honda y un machete, con los que conseguía cada tanto matar algunas palomas o charatas, o cazar un tatú. Lo más frecuente solía ser atrapar algunas aves con las dos o tres cimbras que tenía preparadas en el monte.
										</p>
										<p>
											En esas condiciones, los chicos estaban cada vez más débiles y flaquitos. La pobre madre, desesperada, no sabía qué hacer durante el día ante sus hambrientos hijitos. Para colmo, su bebé, de apenas dos meses, era puro piel y huesos. De tan desnutrido, tal vez no llegaba a pasar ni dos kilos. Casi ni lloraba de lo débil que estaba. 
										</p>
										<p>
											Angustiada, la madre decidió ponerse a buscar un trabajo como empleada doméstica en alguna chacra. Y lo consiguió. Desde ese día, partía a las cuatro de las madrugada cada mañana para estar a las seis en la chacra de los Alviso. Hacia el atardecer, ya estaba de regreso. El padre continuaba con su diario trajinar por toda la zona buscando alguna changuita o comida.
										</p>
										<p>
											Claro, desde el día en que la madre comenzó a salir a trabajar los chicos quedaban solos. El mayor debía hacerse cargo del cuidado de sus hermanitos.
										</p>
										<p>
											A los pocos días ocurrió la desgracia.
										</p>
										<p>
											A eso de las diez de la mañana, tres de los hermanos —el de diez, el de nueve y el de ocho— salieron hacia el estero a buscar, como todos los días, nidos de teros y de perdices. La mamá solía hacer tortillas con los huevos de estas aves. El mayor, con el bebé, y una hermanita de dos años fueron hasta un algarrobo a orillas del estero. Al bebé lo tenían en una especie de cunita hecha con un cajón de manzanas. Se sentaron bajo la sombra del gran árbol entre los pajonales, y ahí estaban, hasta que de pronto, el hermano mayor ve una bandada de palomas que se posaban en un mogote cercano, a unos cien metros. Le dijo a su hermanita que se quedara quieta y calladita cuidando al bebé, hasta que él regresara. Preparó su honda y unos cuantos balines, y sigilosamente se dirigió hacia el mogote. Con un poco de suerte podría matar una y así tendrían algo para comer. 
										</p>
										<p>
											Al mismo tiempo, los tres hermanos que andaban por el estero encontraron un nido de ñacaniná, y como siempre solía ocurrir, no aguantaron la tentación de cascotearlo y salir a correr. La víbora enfurecida comenzó a perseguir a los niños, que pasaron corriendo cerca del algarrobo —hacia el ranchito— sin ver ni advertir que su hermanita y el bebé estaban allí.
										</p>
										<p>
											La nena de dos años, que distraídamente jugaba con unos palitos al lado de la cuna-cajón, de pronto vio asomar entre los pajonales a esa tremenda víbora de casi dos metros, que se acercaba hacia ella con su amenazante cabeza erguida. Muda de espanto, sin siquiera poder llorar, salió a correr hacia el ranchito. Al llegar allí, lloraba con la respiración entrecortada sin poder decir una palabra, mientras los tres hermanos trataban de consolarla, diciéndole que no tuviera miedo, que la víbora no llegaría hasta el ranchito. Entre sollozos ahogados y entrecortados, la nena sólo atinaba a señalar con insistencia hacia el algarrobo, queriendo hacer entender que allí… había quedado el bebé. Los hermanitos pensaban simplemente que había visto a la ñacaniná cerca del algarrobo y que por eso estaba tan asustada.
										</p>
										<p>
											Algunos minutos después, cuando el mayor volvía hacia el algarrobo desplumando a la paloma que había cazado, se encontró con la espantosa y macabra situación que lo dejó paralizado de horror: dentro del cajón, estaba la feroz ñacanina… enrollada como un ovillo sobre el cuerpito y sabanitas del bebé.
										</p>
